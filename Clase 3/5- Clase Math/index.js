/*
La clase Math nos proporciona una serie de métodos que podemos utilizar para
realizar operaciones aritméticas complejas, como por ejemplo la raíz cuadrada
*/

let num = 100;
let raizCuadrada = Math.sqrt(num); // 10

// Pero no es lo único que puedes hacer, existen muchos otros métodos útiles

let n1 = Math.pow(10, 2); // 100 - Eleva a la potencia un número, el segundo parámetro es el exponente
let n2 = Math.round(10.85); // 11 - Redondea un número a su entero más cercano
let n3 = Math.floor(10.96); // 10 - Elimina el punto decimal
let n4 = Math.max(100, 200, 500, 50); // 500 - Devuelve el mayor de los números dados
let n5 = Math.min(100, 200, 500, 50); // 50 - Devuelve el menor de los números dados
let n6 = Math.random(); // Devuelve un número aleatorio entre 0 y 1
let n7 = Math.sin(30); // Devuelve el seno de un número
let n8 = Math.cos(60); // Devuelve el coseno de un número

/*
Tienes también constantes como PI
*/
let pi = Math.PI; // 3.141592653589793

