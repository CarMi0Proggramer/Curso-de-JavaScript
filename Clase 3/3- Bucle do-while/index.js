// El bucle do-while se utiliza para ejecutar una acción al menos una vez
let intentos = 0;
do {
    console.log("Te quedan " + intentos + " intentos");
} while (intentos > 0);

/*
Muy parecido al bucle while, la única diferencia es que para que el bucle while
se ejecute la condición se debe cumplir al menos una vez pero en el do-while
eso no importa ya que al menos una vez se va a ejecutar el código proporcionado
*/
